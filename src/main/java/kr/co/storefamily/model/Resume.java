package kr.co.storefamily.model;

public class Resume {
	private int bno;
	private String title;
	private String work_place;
	private String work_activity;
	private String id;
	private String code;
	private String name;
	private String period;
	private String sign_day;
	private String start_day;
	private String end_day;

	public int getBno() {
		return bno;
	}
	public void setBno(int bno) {
		this.bno = bno;
	}

	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getWork_place() {
		return work_place;
	}
	public void setWork_place(String work_place) {
		this.work_place = work_place;
	}
	public String getWork_activity() {
		return work_activity;
	}
	public void setWork_activity(String work_activity) {
		this.work_activity = work_activity;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getPeriod() {
		return period;
	}
	public void setPeriod(String period) {
		this.period = period;
	}
	public String getSign_day() {
		return sign_day;
	}
	public void setSign_day(String sign_day) {
		this.sign_day = sign_day;
	}
	public String getStart_day() {
		return start_day;
	}
	public void setStart_day(String start_day) {
		this.start_day = start_day;
	}
	public String getEnd_day() {
		return end_day;
	}
	public void setEnd_day(String end_day) {
		this.end_day = end_day;
	}


}
